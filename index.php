            Hmong fabrics are renowned across the world; thanks to their rich colours and intricate designs.
             Their story cloths are particularly celebrated. Prior to the 20th century, the Hmong had no form 
             of written language, and instead, communicated ideas and narratives onto beautifully intricate pieces of cloth.

            This practice wasn’t limited to females – even the male members of the village would create story cloths, 
